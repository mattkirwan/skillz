<h3>GIVE</h3>
<p>A user can give a skill. It consists of the following data:</p>
<ul>
	<li>Skill Topic (eg: Chess, Ice-sculpting, Illustration, Baking, Programming)</li>
	<li>Skill structure
		<ul>
			<li>Custom (allows receiver to request the learning structure)</li>
			<li>Overview (a general introduction to the topic)</li>
		</ul>
	</li>
	<li>Skill Location (eg: Newcastle)</li>
	<li>Skill duration (eg: 6 hours)</li>
</ul>
<p>Upon successful skill transfer the giver will acrue 6 points (for 6 hours). This can then be used to then receive some skillz.</p>